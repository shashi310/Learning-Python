# Arithmetic Operators
x = 10
y = 3
print(x + y)  # Addition
print(x - y)  # Subtraction
print(x * y)  # Multiplication
print(x / y)  # Division
print(x % y)  # Modulo

# Comparison Operators
age = 25
print(age > 18)   # Greater than
print(age <= 30)  # Less than or equal to
print(age == 25)  # Equal to
print(age != 20)  # Not equal to

# Logical Operators
is_raining = True
is_sunny = False
print(is_raining and is_sunny)  # Logical AND
print(is_raining or is_sunny)   # Logical OR
print(not is_raining)           # Logical NOT
